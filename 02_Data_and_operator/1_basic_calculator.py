# Take two numbers as input
a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

# Print sum
print("Sum:", a + b)
# Print difference
print("Difference:", a - b)

# Print product
print("Product:", a * b)

# Print quotient
print("Quotient:", a / b)
# Print remainder
print("Remainder:", a % b)

